
function toggleTextboxmon(checkbox) {
  

  const textbox_elem = document.getElementById('my_text');
  const textbox_elem1 = document.getElementById('my_text1');
  const textbox_elem2 = document.getElementById('my_text2');
  const textbox_elem3 = document.getElementById('my_text3');
  
  textbox_elem.disabled = checkbox.checked ? false : true;
  textbox_elem1.disabled = checkbox.checked ? false : true;
  textbox_elem2.disabled = checkbox.checked ? false : true;
  textbox_elem3.disabled = checkbox.checked ? false : true;
  
  if(textbox_elem.disabled) {
    textbox_elem.value = null;
    textbox_elem1.value = null;
    textbox_elem2.value = null;
    textbox_elem3.value = null;
  }else {
    textbox_elem3.focus();
    textbox_elem2.focus();
    textbox_elem1.focus();
    textbox_elem.focus();
  }

}

